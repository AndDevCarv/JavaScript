const calculadora = function calcular(x, y, operacao) {
    
    switch(calcular) {
        case operacao = '+':
            x + y
            break
        case operacao = '-':
            x - y
            break
        case operacao = '*':
            x * y
            break
        case operacao = '/':
            x / y
            break
        default:
            console.log('Erro');
            
    }
}

calculadora(2,3,'*')